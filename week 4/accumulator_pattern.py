# Given the list of numbers, numbs, create a new list of those same numbers increased by 5.
# Save this new list to the variable newlist.
numbs = [5, 10, 15, 20, 25]
newlist = []

for num in numbs:
    newlist.append(num + 5)
print(newlist)

# Multiply list elements by 2
lst_nums = [4, 29, 5.3, 10, 2, 1817, 1967, 9, 31.32]
larger_nums = []

for num in lst_nums:
    larger_nums.append(num * 2)
print(larger_nums)
